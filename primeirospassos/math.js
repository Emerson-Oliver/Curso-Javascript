/*Algumas funções da classe Math
   Math.ceil | Arredonda numero com ponto para cima
   Math.floor | Arredonda numero com ponto para baixo
   Math.round | Arrendo numero com  ponto para cima ou para baixo a depender do numero apos o ponto ou para cima se o numero apos o ponto for do meio EX: .5
   Math.abs | Retorna valor absoluto
   Math.sqrt | Retona raiz quadrada
   Math.pow | Retorna potenciação
   Math.max | Retorna maior valor de uma sequencia
   Math.min | Retorna menor valor de uma sequencia
*/   

